// objects 
// arrays are good but not suffiecient 
// for real world data 
// objects store key value pairs 
// objects do not have index 
// object is a reference data type 

// how to create objects 

const obj = {
    name : "arpan", // name is key and arpan is value
    age : 18 ,
    city : "durgapur", 
    hobbies : ["coding", "reading", "playing games"] // we can also store arrays in an object 
}



// how to access data fron objects 




console.log(obj);
console.log(obj.name , obj.age); // this is how we access data from an object 



// how to add key value pairs to objects 

obj.gender = "male"; // this is how we add key value pairs to objects
console.log(obj);
